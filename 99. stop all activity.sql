-- storage by table and database, schema
SELECT 
    TABLE_CATALOG AS DATABASE_NAME,
    TABLE_SCHEMA AS SCHEMA_NAME,
    TABLE_NAME,
    ROUND(ACTIVE_BYTES / (1024*1024), 2) AS ACTIVE_STORAGE_MB,
    ROUND(TIME_TRAVEL_BYTES / (1024*1024), 2) AS TIME_TRAVEL_STORAGE_MB,
    ROUND(FAILSAFE_BYTES / (1024*1024), 2) AS FAILSAFE_STORAGE_MB,
    ROUND(RETAINED_FOR_CLONE_BYTES / (1024*1024), 2) AS RETAINED_FOR_CLONE_MB,
    (ACTIVE_STORAGE_MB + TIME_TRAVEL_STORAGE_MB+ FAILSAFE_STORAGE_MB + RETAINED_FOR_CLONE_MB) AS TOTAL_STORAGE_MB
FROM 
    SNOWFLAKE.ACCOUNT_USAGE.TABLE_STORAGE_METRICS
WHERE DATABASE_NAME <> 'SNOWFLAKE'
ORDER BY 
    TOTAL_STORAGE_MB DESC;

-- expensive queries
SELECT
    QUERY_ID,
    QUERY_TEXT,
    USER_NAME,
    WAREHOUSE_NAME,
    START_TIME,
    END_TIME,
    TOTAL_ELAPSED_TIME / 1000 AS EXECUTION_TIME_SECONDS,
    -- Estimated cost calculation: credits per hour * hours executed
    -- This assumes standard Snowflake pricing tiers for simplicity
    (CASE
        WHEN WAREHOUSE_SIZE = 'X-Small' THEN 1
        WHEN WAREHOUSE_SIZE = 'Small' THEN 2
        WHEN WAREHOUSE_SIZE = 'Medium' THEN 4
        WHEN WAREHOUSE_SIZE = 'Large' THEN 8
        WHEN WAREHOUSE_SIZE = 'X-Large' THEN 16
        WHEN WAREHOUSE_SIZE = '2X-Large' THEN 32
        WHEN WAREHOUSE_SIZE = '3X-Large' THEN 64
        WHEN WAREHOUSE_SIZE = '4X-Large' THEN 128
    END) * (TOTAL_ELAPSED_TIME / 1000.0 / 3600.0) AS ESTIMATED_CREDITS_CONSUMED
FROM
    SNOWFLAKE.ACCOUNT_USAGE.QUERY_HISTORY
WHERE
    START_TIME >= DATEADD(day, -1, CURRENT_TIMESTAMP()) -- Filter for the last 7 days
    AND ERROR_CODE IS NULL -- Exclude failed queries
ORDER BY
    ESTIMATED_CREDITS_CONSUMED DESC
LIMIT 10;



