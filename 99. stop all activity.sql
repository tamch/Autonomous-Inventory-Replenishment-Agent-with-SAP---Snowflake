-- Query to combine current table sizes with daily database storage increases
WITH DailyDatabaseGrowth AS (
    SELECT
        USAGE_DATE,
        DATABASE_NAME,
        -- Calculate daily increase in storage bytes
        AVERAGE_DATABASE_BYTES - LAG(AVERAGE_DATABASE_BYTES, 1, AVERAGE_DATABASE_BYTES) OVER (
            PARTITION BY DATABASE_NAME
            ORDER BY USAGE_DATE
        ) AS DAILY_INCREASE_BYTES
    FROM
        SNOWFLAKE.ACCOUNT_USAGE.DATABASE_STORAGE_USAGE_HISTORY
    WHERE
        USAGE_DATE >= CURRENT_DATE() - INTERVAL '30 days' -- Adjust the date range as needed (max 1 year history)
),
CurrentTableSizes AS (
    SELECT
        TABLE_CATALOG AS DATABASE_NAME,
        TABLE_SCHEMA,
        TABLE_NAME,
        -- Combine active and time travel bytes for total current size
        (ACTIVE_BYTES + TIME_TRAVEL_BYTES + FAILSAFE_BYTES) AS TOTAL_BYTES
    FROM
        SNOWFLAKE.ACCOUNT_USAGE.TABLE_STORAGE_METRICS
    WHERE
        DELETED IS NULL -- Filter out dropped tables not in failsafe
)
SELECT
    DDG.USAGE_DATE,
    CTS.DATABASE_NAME,
    CTS.TABLE_SCHEMA,
    CTS.TABLE_NAME,
    CTS.TOTAL_BYTES AS CURRENT_SIZE_BYTES,
    DDG.DAILY_INCREASE_BYTES -- This is the total database daily increase, not specific to the table
FROM
    CurrentTableSizes CTS
JOIN
    DailyDatabaseGrowth DDG ON CTS.DATABASE_NAME = DDG.DATABASE_NAME
ORDER BY
    DDG.USAGE_DATE DESC,
    CTS.DATABASE_NAME,
    CTS.TABLE_SCHEMA,
    CTS.TABLE_NAME;
    
